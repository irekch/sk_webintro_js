// JavaScript File

var x = 5;

// ++   zwiekszenie wartosci o 1
document.write('wartosc poczatkowa x = ', x, '<br>');
document.write('x++ czyli post-incrementacja, wyswietla ', x++, ' potem zwieksza');
document.write('<br>');
document.write('wartosc z postinrementacji, czyli 6 jest zwiekszona przez ++x ', 
                'co daje ', ++x);
                
// -- operator dekrementacji
document.write('<br>');
document.write('dekrementacja --x = ', --x);



/* Wyniki-Wydruk

wartosc poczatkowa x = 5
x++ czyli post-incrementacja, wyswietla 5 potem zwieksza
wartosc z postinrementacji, czyli 6 jest zwiekszona przez ++x co daje 7
dekrementacja --x = 6 

*/